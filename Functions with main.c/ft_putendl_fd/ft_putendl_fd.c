#include "libft.h"
#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	int l;

	l = 0;
	while (s[l])
	{
		ft_putchar_fd(s[l], fd);
		l++;
	}
	write(1, "\n", 1);
}